	;子模块3
	;本模块显示最大为FFFFH的十六进制数
	;AX=待显示的四位十六进制数
	EXTRN KASC:BYTE
	PUBLIC SHOWHEX
	.MODEL SMALL
	.CODE
SHOWHEX	PROC	FAR
	PUSHF
	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX
	PUSH DI
	
	;MOV AX,0030H
	MOV BX,AX
	MOV DI,4;计数器值为4时可以表示BX
LP:	MOV CL,4
	ROL BX,CL
	MOV AX,BX
	OR AL,30H;分别先取高四位后取低四位
	CMP AL,3AH;判断是否是大于0AH
	JGE SHOW1
	MOV AH,02
	MOV DL,AL
	INT 21H
	JMP NEXT
SHOW1:
	ADD AL,07H
	ADD AL,40H
	MOV AH,02
	MOV DL,AL
	INT 21H
NEXT:
	DEC DI
	JNZ LP
    ;表示是十六进制数
    ;MOV AH,02
    ;MOV DX,'H'
    ;INT 21H
	
	POP DI
	POP DX
	POP CX
	POP BX
	POP AX
	POPF
	RET
SHOWHEX	ENDP
	END





